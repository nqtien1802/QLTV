    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int numRowSelected = tblSach.getSelectedRow();
        if (numRowSelected < 0) {
            JOptionPane.showMessageDialog(null, "Bạn chưa chọn sách nào !", "Cảnh báo", 2);
            return;
        }
        if (!check()) {
            JOptionPane.showMessageDialog(null, "Sách đã được chọn!", "Cảnh báo", 2);
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tblSachDuocChon.getModel();
        Object[] objRow = new Object[2];
        objRow[0] = tblSach.getValueAt(numRowSelected, 0);
        objRow[1] = tblSach.getValueAt(numRowSelected, 1);

        model.addRow(objRow);
        tinhTienCoc();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void txtTimKiemSachKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTimKiemSachKeyReleased
        String sql = "";
        String kieuTimKiemSach = "";
        switch (cbxTimKiemSach.getSelectedItem().toString()) {
            case "Mã số sách":
                kieuTimKiemSach = "MaSoSach";
                break;
            case "Tên sách":
                kieuTimKiemSach = "TenSach";
                break;
            case "Nhà xuất bản":
                kieuTimKiemSach = "NhaXuatBan";
                break;
            case "Thể loại":
                kieuTimKiemSach = "TheLoai";
                break;
            case "Tác giả":
                kieuTimKiemSach = "TacGia";
                break;
        }
        sql = "select * from sach where " + kieuTimKiemSach + " like '%"
                + txtTimKiemSach.getText() + "%';";
        hienThiSach(sql);
    }//GEN-LAST:event_txtTimKiemSachKeyReleased

