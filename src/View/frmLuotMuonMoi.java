    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String path = "";
        if (tblSachDuocChon.getRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "Bạn chưa chọn sách nào !");
            return;
        }
        java.sql.Date ngayMuon = quanLyThuVien.dateToDate(new Date());
        java.sql.Date hanTra = new java.sql.Date(dcsHanTra.getDate().getTime());
        java.sql.Date ngayTra = null;
        if (hanTra == null) {
            JOptionPane.showMessageDialog(null, "Hạn trả không chính xác !");
            return;
        }
        MuonTra muonTra = new MuonTra(txtMaSoMuon.getText(), txtMaSoBanDoc.getText(),
                ngayMuon, hanTra, ngayTra, txtMaSoNhanVien.getText(), 0, soTienCoc);

        if (quanLyThuVien.addMuonTra(muonTra)) {
            for (int i = 0; i < tblSachDuocChon.getRowCount(); i++) {
                ChiTietMuon chiTietMuon = new ChiTietMuon(txtMaSoMuon.getText(),
                        tblSachDuocChon.getValueAt(i, 0).toString(), "Đang mượn");
                if (!quanLyThuVien.addChiTietMuon(chiTietMuon)) {
                    JOptionPane.showMessageDialog(null, "Thêm sách " + tblSachDuocChon.getValueAt(i, 0).toString()
                            + " không thành công !");
                }
            }
            JFileChooser fcs = new JFileChooser();
            if (fcs.showSaveDialog(this) == JFileChooser.APPROVE_OPTION) {
                path = fcs.getSelectedFile().getAbsolutePath();
                if (quanLyThuVien.inPhieuMuon(banDoc, muonTra, nhanVien, tblSachDuocChon, path)) {
                    JOptionPane.showMessageDialog(null, "In phiếu thành công !");
                    main.hienThiMuonTra("select * from muontra where TrangThai = 1");
                    try {
                        Desktop.getDesktop().open(new File(path + ".pdf"));
                    } catch (IOException ex) {
                        Logger.getLogger(frmLuotMuonMoi.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "In phiếu không thành công !");
                }

            }
        }

    }//GEN-LAST:event_jButton3ActionPerformed

