    private void txtTimKiemBanDocKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTimKiemBanDocKeyReleased
        String kieuTimKiem = "";
        String noiDungTimKiem = "";
        switch (cbxTimKiemBanDoc.getSelectedItem().toString()) {
            case "Mã số":
                kieuTimKiem = "MaSoBanDoc";
                break;
            case "Họ tên":
                kieuTimKiem = "HoTen";
                break;
            case "Giới tính":
                kieuTimKiem = "GioiTinh";
                break;
            case "Năm sinh":
                kieuTimKiem = "NamSinh";
                break;
            case "Địa chỉ":
                kieuTimKiem = "DiaChi";
                break;
            case "Số chứng minh thư":
                kieuTimKiem = "SoChungMinhThu";
                break;
            case "Số điện thoại":
                kieuTimKiem = "SoDienThoai";
                break;
            case "Email":
                kieuTimKiem = "Email";
                break;
        }
        if (kieuTimKiem.equals("NamSinh")) {
            if (txtTimKiemBanDoc.getText().equals("")) {
                return;
            }
            try {
                int i = Integer.parseInt(txtTimKiemBanDoc.getText());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Nhập năm không hợp lệ");
                return;
            }

            noiDungTimKiem = " = " + txtTimKiemBanDoc.getText();
        } else {
            noiDungTimKiem = " like '%" + txtTimKiemBanDoc.getText() + "%'";
        }
        String sql = "select * from bandoc where " + kieuTimKiem + noiDungTimKiem + " and TrangThai = 1 order by MaSoBanDoc asc;";
        hienThiBanDoc(sql);
    }//GEN-LAST:event_txtTimKiemBanDocKeyReleased

