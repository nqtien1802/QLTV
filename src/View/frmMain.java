    private int tinhTienPhat() {
        int tienPhat = 0;
        int soSachMat = 0;
        Date ngayHoanThanh = dcsNgayTra.getDate();
        int numRowSelected = tblMuonTra.getSelectedRow();
        for (int i = 0; i < tblSach.getRowCount(); i++) {
            for (int j = 0; j < tblChiTietSach.getRowCount(); j++) {
                if (tblSach.getValueAt(i, 0).toString().equals(tblChiTietSach.getValueAt(j, 1))
                        && tblChiTietSach.getValueAt(j, 2).equals("Mất")) {
                    soSachMat++;
                    tienPhat += 2 * (int) tblSach.getValueAt(i, 6);
                }
            }
        }
        java.util.Date date1 = new java.util.Date();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        try {
            date1 = sdf.parse(tblMuonTra.getValueAt(numRowSelected, 3).toString());
        } catch (ParseException ex) {
            Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE, null, ex);
        }

        int soNgayMuon = (int) ((ngayHoanThanh.getTime() - date1.getTime()) / 1000 / 86400); // Số ngày muộn
        int soSachMuon = tblChiTietSach.getRowCount() - soSachMat; // Số sách trả muộn
        if (soNgayMuon > 0) {
            tienPhat += soNgayMuon * soSachMuon * 5000;
        }
        return tienPhat;
    }

