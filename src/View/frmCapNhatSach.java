    private boolean check() {
        if (txtMaSoSach.getText().equals("") || txtTenSach.getText().equals("")
                || txtNhaXuatBan.getText().equals("") || txtTacGia.getText().equals("")
                || txtNamXuatBan.getText().equals("")
                || txtTheLoai.getText().equals("") || txtGiaSach.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập đầy đủ thông tin sách",
                    "Cảnh báo", 2);
            return false;
        }
        try {
            int year = Integer.parseInt(txtNamXuatBan.getText());
            if (year < 0) {
                JOptionPane.showMessageDialog(rootPane, "Nhập năm không đúng định dạng !",
                        "Cảnh báo", 2);
                return false;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Nhập năm không đúng định dạng !",
                    "Cảnh báo", 2);
            return false;
        }
        try {
            int gia = Integer.parseInt(txtGiaSach.getText());
            if (gia < 0) {
                JOptionPane.showMessageDialog(rootPane, "Nhập giá sách không đúng định dạng !",
                        "Cảnh báo", 2);
                return false;
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Nhập giá sách không đúng định dạng !",
                    "Cảnh báo", 2);
            return false;
        }
        return true;
    }

