    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        if (!check()) {
            return;
        }
              
        Sach sachMoi = new Sach(txtMaSoSach.getText(), txtTenSach.getText(),
                txtTacGia.getText(), txtNhaXuatBan.getText(), Integer.parseInt(txtNamXuatBan.getText()),
                txtTheLoai.getText(), Integer.parseInt(txtGiaSach.getText()), 1);
        
        
        if(sach == null){
            if(quanLyThuVien.addSach(sachMoi)){
                JOptionPane.showMessageDialog(this, "Thêm sách mới thành công!");
            }
            else{
                 JOptionPane.showMessageDialog(this, "Thêm sách không thành công!");
            }
        }
        if(sach != null){
            if(quanLyThuVien.suaSach(sachMoi)){
                 JOptionPane.showMessageDialog(this, "Sửa thành công!");
            }
            else{
                System.out.println("Sửa không thành công!");
            }
        }
       main.hienThiSach("select * from sach where TrangThai = 1;");
    }//GEN-LAST:event_btnCapNhatActionPerformed

