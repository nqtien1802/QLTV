    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        buttonGroup1.add(btgNam);
        buttonGroup1.add(btgNu);
        setLocationRelativeTo(null);
        if (banDoc == null) {
            this.setTitle("Thêm mới");
            btnCapNhat.setText("Thêm");
            txtMaSo.setEditable(true);
        } else {
            this.setTitle("Sửa thông tin");
            btnCapNhat.setText("Sửa");
            txtMaSo.setText(banDoc.getMaSo());
            txtMaSo.setEditable(false);
            txtHoTen.setText(banDoc.getHoTen());
            if (banDoc.getGioiTinh().equals("Nam")) {
                btgNam.setSelected(true);
                btgNu.setSelected(false);
            } else {
                btgNu.setSelected(true);
                btgNam.setSelected(false);
            }
            txtNamSinh.setText(banDoc.getNamSinh() + "");
            txtDiaChi.setText(banDoc.getDiaChi());
            txtSoChungMinhThu.setText(banDoc.getSoChungMinhThu());
            txtSoDienThoai.setText(banDoc.getSoDienThoai());
            txtEmail.setText(banDoc.getEmail());

        }
    }//GEN-LAST:event_formWindowOpened

