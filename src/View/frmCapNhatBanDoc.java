    // According to the maximum ID of lstBanDoc, create a new ID what next
    // to the old ID
    private String newMaSo(String maSoCu) {
        String MaSoMoi = "";
        if (maSoCu == "") {
            return "BD1000";
        }
        String number = maSoCu.substring(2);
        int soThuTu = Integer.parseInt(number);
        soThuTu++;
        MaSoMoi = "BD" + soThuTu;
        return MaSoMoi;
    }

    // Check the format of info before do everything
    private boolean check() {
        if (txtHoTen.getText().equals("") || txtNamSinh.getText().equals("") || txtDiaChi.getText().equals("")
                || txtSoChungMinhThu.getText().equals("") || txtSoDienThoai.getText().equals("")
                || txtEmail.getText().equals("") || (!btgNam.isSelected() && !btgNu.isSelected())) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin của bạn đọc");
            return false;
        }
        try {
            int namSinh = Integer.parseInt(txtNamSinh.getText());
            if (namSinh < 1900 || namSinh >2003) {
                JOptionPane.showMessageDialog(this, "Năm sinh không đúng !");
                return false;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Nhập năm không hợp lệ !");
            return false;
        }

        try {
            double sdt = Double.parseDouble(txtSoDienThoai.getText());
            if (sdt < 9) {
                JOptionPane.showMessageDialog(this, "Số điện thoại không đúng !");
                return false;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Nhập số điện thoại không hợp lệ !");
            return false;
        }

        try {
            double cmt = Double.parseDouble(txtSoChungMinhThu.getText());
            if (cmt < 0) {
                JOptionPane.showMessageDialog(this, "Số chứng minh thư có 9 số!");
                return false;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Nhập số chứng minh thư không hợp lệ !");
            return false;
        }
        return true;
    }

    // Clear the old Data to be realdy to add a new BanDoc
    private void xoaDuLieu() {
        txtDiaChi.setText("");
        txtEmail.setText("");
        txtHoTen.setText("");
        txtMaSo.setText(maSoMoi);
        txtNamSinh.setText("");
        txtSoChungMinhThu.setText("");
        txtSoDienThoai.setText("");
        btgNam.setSelected(false);
        btgNu.setSelected(false);
    }

