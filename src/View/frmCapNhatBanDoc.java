    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        if (!check()) {
            return;
        }
        BanDoc banDoc2 = new BanDoc();
        String gioiTinh = "";
        if (btgNam.isSelected()) {
            gioiTinh = "Nam";
        } else if (btgNu.isSelected()) {
            gioiTinh = "Nữ";
        }
        banDoc2.setMaSo(txtMaSo.getText());
        banDoc2.setHoTen(txtHoTen.getText());
        banDoc2.setGioiTinh(gioiTinh);
        banDoc2.setNamSinh(Integer.parseInt(txtNamSinh.getText()));
        banDoc2.setDiaChi(txtDiaChi.getText());
        banDoc2.setSoChungMinhThu(txtSoChungMinhThu.getText());
        banDoc2.setSoDienThoai(txtSoDienThoai.getText());
        banDoc2.setEmail(txtEmail.getText());

        if (banDoc == null) {
            if (quanLyThuVien.addBanDoc(banDoc2)) {
                JOptionPane.showMessageDialog(this, "Thêm thành công !", "Sửa thành công",1,null);
                main.hienThiBanDoc("select * from bandoc where TrangThai = 1 order by MaSoBanDoc asc;");
                xoaDuLieu();
                return;
            } else {
                JOptionPane.showMessageDialog(this, "Thêm không thành công !", "Thêm không thành công",2,null);
            }
        } else if (quanLyThuVien.suaBanDoc(banDoc2)) {
            JOptionPane.showMessageDialog(this, "Sửa thành công !", "Sửa thành công",1,null);
            main.hienThiBanDoc("select * from bandoc where TrangThai = 1 order by MaSoBanDoc asc;");
            return;
        } else {
            JOptionPane.showMessageDialog(this, "Sửa không thành công !", "Sửa không thành công",2,null);
        }
    }//GEN-LAST:event_btnCapNhatActionPerformed

